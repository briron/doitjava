package doitjava.binary;

public class BinaryTest {
    public static void main(String[] args) {
        int num = 10;
        int bNum = 0b1010;
        int oNum = 012;
        int hNum = 0xA;

        System.out.println("num = " + num);
        System.out.println("bNum = " + bNum);
        System.out.println("oNum = " + oNum);
        System.out.println("hNum = " + hNum);
    }
}
